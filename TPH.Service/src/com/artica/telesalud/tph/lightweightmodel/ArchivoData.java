package com.artica.telesalud.tph.lightweightmodel;

import java.io.Serializable;

public class ArchivoData implements Serializable{
	
	private Integer archivoDataId;
	private String tipoContenido;
	private String nombreArchivo;
	private String nombreOriginalArchivo;
	
	public Integer getArchivoDataId() {
		return archivoDataId;
	}
	public void setArchivoDataId(Integer archivoDataId) {
		this.archivoDataId = archivoDataId;
	}
	public String getTipoContenido() {
		return tipoContenido;
	}
	public void setTipoContenido(String tipoContenido) {
		this.tipoContenido = tipoContenido;
	}
	public String getNombreArchivo() {
		return nombreArchivo;
	}
	public void setNombreArchivo(String nombreArchivo) {
		this.nombreArchivo = nombreArchivo;
	}
	public String getNombreOriginalArchivo() {
		return nombreOriginalArchivo;
	}
	public void setNombreOriginalArchivo(String nombreOriginalArchivo) {
		this.nombreOriginalArchivo = nombreOriginalArchivo;
	}
	
}
